package com.lingc.coolapkconsole.model;

/**
 * Created by Trumeet on 2017/3/13.
 * A download stat item.
 * @author Trumeet
 */

public class DownloadStatItem {
    private String mDate;
    private String mVersion;
    private String mAppName;
    private String mDownloads;
    private String mDownloadsStation;
    private String mDownloadsOutsideStation;
    private String mDownloadsNew;
    private String mInstalls;
    private String mInstallsNew;
    private String mSize;

    public String getmDate() {
        return mDate;
    }

    public void setmDate(String mDate) {
        this.mDate = mDate;
    }

    public String getmAppName() {
        return mAppName;
    }

    public void setmAppName(String mAppName) {
        this.mAppName = mAppName;
    }

    public String getmDownloads() {
        return mDownloads;
    }

    public void setmDownloads(String mDownloads) {
        this.mDownloads = mDownloads;
    }

    public String getmDownloadsStation() {
        return mDownloadsStation;
    }

    public void setmDownloadsStation(String mDownloadsStation) {
        this.mDownloadsStation = mDownloadsStation;
    }

    public String getmDownloadsOutsideStation() {
        return mDownloadsOutsideStation;
    }

    public void setmDownloadsOutsideStation(String mDownloadsOutsideStation) {
        this.mDownloadsOutsideStation = mDownloadsOutsideStation;
    }

    public String getmDownloadsNew() {
        return mDownloadsNew;
    }

    public void setmDownloadsNew(String mDownloadsNew) {
        this.mDownloadsNew = mDownloadsNew;
    }

    public String getmInstalls() {
        return mInstalls;
    }

    public void setmInstalls(String mInstalls) {
        this.mInstalls = mInstalls;
    }

    public String getmInstallsNew() {
        return mInstallsNew;
    }

    public void setmInstallsNew(String mInstallsNew) {
        this.mInstallsNew = mInstallsNew;
    }

    public String getmSize() {
        return mSize;
    }

    public void setmSize(String mSize) {
        this.mSize = mSize;
    }

    public String getmVersion() {
        return mVersion;
    }

    public void setmVersion(String mVersion) {
        this.mVersion = mVersion;
    }
}
